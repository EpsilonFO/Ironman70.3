# Fonctions utilitaires
def save_data(data, filename):
    """Sauvegarde les donn√©es dans un fichier JSON"""
    with open(filename, 'w') as f:
        json.dump(data, f, default=str)

def load_data(filename):
    """Charge les donn√©es depuis un fichier JSON"""
    if os.path.exists(filename):
        with open(filename, 'r') as f:
            return json.load(f)
    return {}

def get_current_week():
    """Calcule la semaine actuelle du programme"""
    start_date = date(2025, 6, 29)  # Date de d√©but
    current_date = date.today()
    days_diff = (current_date - start_date).days
    week = min((days_diff // 7) + 1, 3)  # Maximum 3 semaines dans le programme
    return max(1, week)

def get_current_day():
    """Retourne le jour actuel en fran√ßais"""
    days = ['Lundi', 'Mardi', 'Mercredi', 'Jeudi', 'Vendredi', 'Samedi', 'Dimanche']
    return days[date.today().weekday()]

